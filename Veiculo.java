public class Veiculo {
    protected String modelo;
    protected String cor;
    protected String marca;
    protected int ano;
    protected String placa;

    public Veiculo(String modelo, String cor, String marca, int ano, String placa){
        this.modelo = modelo;
        this.cor = cor;
        this.marca = marca;
        this.ano = ano;
        this.placa = placa;

    }

}

